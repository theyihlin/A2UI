import * as fs from 'fs';
import * as path from 'path';

const OUTPUT_DIR = path.resolve('generated');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'catalogs-registry.ts');

console.log('⚡ A2UI Unified Catalog Compiler running...');
console.log('📂 Generating static catalog registries into:', OUTPUT_FILE);

// Create output folder if not exists
if (!fs.existsSync(OUTPUT_DIR)) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
}

const registryContent = `/*
 * Generated by A2UI Pre-Build Catalog Compiler.
 * DO NOT EDIT THIS FILE DIRECTLY.
 */

import { Catalog } from '@a2ui/web_core/v0_9';
import { basicCatalog } from '@a2ui/lit/v0_9';

// Static Imports of custom components resolved from the catalogs
import { WeatherWidget } from '../components/weather-widget.js';
import { McpApp } from '../components/mcp-app.js';

const basicComponents = Array.from(basicCatalog.components.values());

export const clientBasicCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/basic-catalog.json',
  basicComponents
);

export const clientWeatherCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/weather-catalog.json',
  [
    ...basicComponents,
    WeatherWidget as any
  ]
);

export const clientMcpCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/mcp-catalog.json',
  [
    ...basicComponents,
    McpApp as any
  ]
);

export const catalogsRegistry = {
  basic: clientBasicCatalog,
  weather: clientWeatherCatalog,
  mcp: clientMcpCatalog
};

export type CatalogKey = 'basic' | 'weather' | 'mcp';
`;

fs.writeFileSync(OUTPUT_FILE, registryContent, 'utf-8');
console.log('✓ Static catalog registries generated successfully!');
